package operadores;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int a = 24;
		int b = 'a';
		
		System.out.println(a == b);
		
		System.out.println(3 >  4);
		System.out.println(3 >= 3);
		System.out.println(3 <  7);
		System.out.println(3 <= 7);
		System.out.println(9 != 7);
		
	}
}